const mongoose = require("mongoose");

const userSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    role: {
        type: String,
        enum: ["admin", "hr", "supervisor", "employee"],
        default: "employee"
    }
})

const User = mongoose.model('user', userSchema);

module.exports = User;